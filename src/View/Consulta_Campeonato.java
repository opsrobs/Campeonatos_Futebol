    public void clearSc() {
        if (ComboCampeonato.isDisplayable()) {
            ComboCampeonato.setSelectedIndex(-1);
        }
        TxtNome.setText("");
        TxtDataInicio.setText("");
        TxtDataFim.setText("");
        TxtCategoria.setText("");
        TxtNome.requestFocus();
        JBtnLimpar.setEnabled(false);

    }

    private void cadastrarCategoria() {
        try {
            if (ComboCampeonato.getSelectedIndex() >= 0) {

                Categoria categoria = (Categoria) ComboCampeonato.getSelectedItem();
                assert categoria != null;
                categoria.setNome(TxtCategoria.getText());
                sbcat.update(categoria);

            } else {
                Categoria categoria = new Categoria(TxtCategoria.getText());
                sbcat.insert(categoria);
            }
            clearSc();
            utils.atualizarCampeonato(ComboCampeonato, sb);

        } catch (SQLException ex) {
            Logger.getLogger(Consulta_Campeonato.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
