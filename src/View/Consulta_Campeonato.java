    private void ComboCampeonatoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ComboCampeonatoItemStateChanged
        if (ComboCampeonato.getItemCount() <= 0) {
            return;
        }
        if (ComboCampeonato.getSelectedIndex() < 0) {
            return;
        }
        campeonato = (Campeonato) ComboCampeonato.getSelectedItem();
        assert campeonato != null;
        TxtNome.setText(campeonato.getNome());
        TxtDataFim.setText(utils.getDataFormatada(campeonato.getDataFim()));
        TxtDataInicio.setText(utils.getDataFormatada(campeonato.getDataInicio()));

        JBtnLimpar.setEnabled(true);

    }//GEN-LAST:event_ComboCampeonatoItemStateChanged

    private void ComboCategoria2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ComboCategoria2ItemStateChanged

        if (ComboCategoria2.getItemCount() <= 0) {
            return;
        }
        if (ComboCategoria2.getSelectedIndex() < 0) {
            return;
        }
        Categoria categoria = (Categoria) ComboCategoria2.getSelectedItem();
        assert categoria != null;
        TxtCategoria.setText(categoria.getNome());
        JBtnLimpar.setEnabled(true);

        try {
            utils.atualizarCampeonatoByCategoria(ComboCampeonato, sb, categoria.getCodigo());
        } catch (SQLException ex) {
            Logger.getLogger(Consulta_Campeonato.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ComboCategoria2ItemStateChanged

    private void JbExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JbExcluirMouseClicked
        if (ComboCampeonato.getItemCount() <= 0) {
            return;
        }
        if (ComboCampeonato.getSelectedIndex() < 0) {
            return;
        }
        Campeonato campeonato = (Campeonato) ComboCampeonato.getSelectedItem();
        assert campeonato != null;
        TxtCategoria.setText(categoria.getNome());
        JBtnLimpar.setEnabled(true);
        int del = JOptionPane.showConfirmDialog(null, "VocÃª deseja realmente excluir o registro " + campeonato, "Excluir...",
                JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, new ImageIcon("C:\\Users\\PremierSoft\\IdeaProjects\\Campeonatos_Futebol\\src\\View\\delete.gif"));
        if (del == JOptionPane.YES_OPTION) {
            try {
                sb.delete(campeonato);
                utils.atualizarCampeonato(ComboCampeonato, sb);
                clearSc();
            } catch (SQLException ex) {
                Logger.getLogger(Consulta_Clube.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(
                    null, "Excluido com sucesso",
                    "Cadastro!!!", JOptionPane.INFORMATION_MESSAGE,
                    new ImageIcon("C:\\Users\\PremierSoft\\IdeaProjects\\Campeonatos_Futebol\\src\\View\\deleted.gif"));
        } else if (del == JOptionPane.NO_OPTION) {
            clearSc();
        }

    }//GEN-LAST:event_JbExcluirMouseClicked

