    public int gols(JTextField g, JTextField g1, JTextField g2, JTextField g3, JTextField g4) {
        int gc = 0, gc1 = 0, gc2 = 0, gc3 = 0, gc4 = 0;
        if (!g.getText().isBlank()) {
            gc = Integer.parseInt(g.getText());
        }
        if (!g1.getText().isBlank()) {
            gc1 = Integer.parseInt(g1.getText());
        }
        if (!g2.getText().isBlank()) {
            gc2 = Integer.parseInt(g2.getText());
        }
        if (!g3.getText().isBlank()) {
            gc3 = Integer.parseInt(g3.getText());
        }
        if (!g4.getText().isBlank()) {
            gc4 = Integer.parseInt(g4.getText());
        }
        return gc + gc1 + gc2 + gc3 + gc4;
    }

